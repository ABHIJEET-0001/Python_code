# Rules for writing variables in Python
# 1. Variable names can only contain letters, numbers, and underscores.
# 2. Variable names cannot start with a number.
# 3. Variable names are case-sensitive (e.g., 'a' and 'A
# are different variables).
# 4. Variable names cannot be a Python keyword (e.g., 'if', 'for', 'while', etc.).
# 5. Variable names should be descriptive and meaningful.   
# 6. Avoid using special characters (e.g., @, $, %, etc.) in variable names.    
# 7. Use lowercase letters for variable names, and separate words with underscores (e.g., 'my_variable').
# 8. Keep variable names short but descriptive enough to understand their purpose.  
# 9. Avoid using single-letter variable names except for loop counters or temporary variables.
# 10. Use underscores to separate words in variable names for better readability (e.g., 
# 'my_variable_name' instead of 'myvariablename').
# 11. Avoid using reserved words or built-in function names as variable names (e.g., 'print', 'list', 'str', etc.).
# 12. Use meaningful names that reflect the purpose of the variable (e.g., 'total_price' instead of 'tp').